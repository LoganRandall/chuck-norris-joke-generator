// PRIMITIVEs

// string
const name = 'John Doe';
console.log(typeof name)

// number
const age = 45
console.log(typeof age)

// boolean
const hasKids = true
console.log(typeof hasKids);

// Null
const car = null;
console.log(typeof car);

// undefined
let test;
console.log(typeof test);

// symbol
const sym = Symbol();
console.log(typeof sym);


// REFERENCE TYPE ( all come back as objects)

// array
const hobbies = ['movies', 
'music'];

console.log(typeof hobbies);


// Object literal
const address = {
  city: 'boston',
  state: 'MA'
}

console.log(typeof address);

// Date
const today = new Date();
console.log(today);
console.log(typeof today);